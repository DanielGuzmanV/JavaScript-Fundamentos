// Podemos asignar el mismo valor:
// Ejemplo suma:
let number1 = 5;
number1 = number1 + 10;
number1 += 5;
console.log('Resultado suma:', number1);
console.log("-----------------------------------------------");

// Ejemplo resta:
let number2 = 30;
number2 = number2 - 5;
number2 -= 10;
console.log('Resultado resta:', number2);
console.log("-----------------------------------------------");

// Ejemplo multiplicacion:
let number3 = 10;
number3 = number3 * 5;
number3 *= 3;
console.log('Resultado multiplicacion:', number3);
console.log("-----------------------------------------------");

// Ejemplo division:
let number4 = 20;
number4 = number4 / 2;
number4 /= 2;
console.log('Resultado division:', number4);
console.log("-----------------------------------------------");

// Ejemplo modulo:
let number5 = 55;
number5 = number5 % 2;
number5 %= 2;
console.log('Resultado modulo:', number5);
console.log("-----------------------------------------------");

// Ejemplo potencia:
let number6 = 20;
number6 = number6 ** 2;
number6 **= 2;
console.log('Resultado potencia:', number6);

