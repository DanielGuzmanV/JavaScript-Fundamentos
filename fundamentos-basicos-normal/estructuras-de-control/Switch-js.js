//Sentencias Switch 
let nota = 14;

switch (nota) {
    case 5:
        console.log("Muy bien");
        break;
    case 4:
        console.log("Bien");
        break;
    case 3: 
        console.log("Regular");
        break;
    case 2:
        console.log("Puede mejorar");
        break;
    case 1:
        console.log("Muy mal...");
        break;
    default:
        console.log("Error");
        break;  
}